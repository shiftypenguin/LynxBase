#define _n(x) (sizeof(x)/sizeof(x[0]))

static inline void inc64le(uint64_t *x, size_t l)
{
	size_t i;

	for (i = 0; i < l; i++) {
		x[i] = (x[i] + (uint64_t)1) & (uint64_t)~0;
		if (x[i]) break;
	}
}

static inline void add64le(uint64_t *x, uint64_t *y, size_t l)
{
	size_t i, f = 0;
	uint64_t t;

	for (i = 0; i < l; i++) {
		t = x[i];
		x[i] += y[i]; x[i] &= (uint64_t)~0;
		if (x[i] < t) {
_again:
			f++;
			t = x[i-f];
			x[i-f]++;
			if (x[i-f] < t) goto _again;
			else f = 0;
		}
	}
}

typedef struct {
	uint64_t K[17]; // key
	uint64_t T[3]; // tweak
	uint64_t ctr[16]; // counter
} tf1024_ctx;

#define THREEFISH_PARITY 0x1bd11bdaa9fc1a22ULL

static void tfs1024_init(tf1024_ctx *ctx)
{
	size_t i;

	ctx->K[_n(ctx->K) - 1] = THREEFISH_PARITY;
	for (i = 0; i < _n(ctx->K) - 1; i++)
		ctx->K[_n(ctx->K)-1] ^= ctx->K[i];

	ctx->T[2] = ctx->T[0] ^ ctx->T[1];
}

